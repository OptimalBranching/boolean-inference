#!/usr/bin/env python3
"""Generate the committed bank manifest (benchmarks/bank_manifest.json).

The manifest is the provenance record for the whole counting bank: per family it
states source URL(s), license, format, conversion, and — once materialized — the
archive sha256 and instance/count tallies read back from each family's
`bank/<id>/provenance.json`. Committing it means a reviewer sees the full plan
(and, after an HPC2 materialize run, the exact hashes) without touching the
gitignored data.

Two-phase by design (matches instances_gen.py / instances_check.py):
  * run this NOW (no data present) -> a manifest with `materialized: false` and
    null hashes: the committed plan.
  * run it again on HPC2 AFTER the fetch_*.py scripts -> the same file with real
    archive sha256s + counts merged in from the provenance sidecars.

Usage:
  bank_manifest_gen.py             # write benchmarks/bank_manifest.json
  bank_manifest_gen.py --dry-run   # print the family summary only
"""
import argparse
import datetime
import json
import os
import sys

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
import bank_common as bc  # noqa: E402
from bank_families import EXTERNAL, GENERATOR  # noqa: E402

MANIFEST = os.path.join(bc.ROOT, "benchmarks", "bank_manifest.json")


def external_entry(fam):
    prov = bc.read_provenance(fam["id"])
    source = {}
    if fam.get("zenodo"):
        source["zenodo_record"] = fam["zenodo"]
        source["zenodo_url"] = f"https://zenodo.org/records/{fam['zenodo']}"
    if fam.get("urls"):
        source["urls"] = fam["urls"]
    entry = {
        "id": fam["id"],
        "kind": "external",
        "priority": fam["priority"],
        "title": fam["title"],
        "source": source,
        "license": fam["license"],
        "format": fam["fmt"],
        "conversion": fam["conversion"],
        "tracks": fam.get("tracks"),
        "reference": fam["reference"],
        "deferred_until_m3": fam.get("deferred", False),
        "notes": fam["notes"],
        "materialized": prov is not None,
        "archive_sha256": (prov or {}).get("archives"),
        "materialize_stats": {k: v for k, v in (prov or {}).items()
                              if k not in ("archives", "family_id")} or None,
    }
    return entry


def generator_entry(fam):
    return {
        "id": fam["id"],
        "kind": "generator",
        "mechanism": fam["mechanism"],
        "generator": fam["generator"],
        "format": fam["fmt"],
        "conversion": fam["conversion"],
        "grid": fam["grid"],
        "counting_role": fam["counting_role"],
        "provenance": "generator + declared seed grid (regenerate to materialize; "
                      "no pre-generated data committed)",
        "seed_note": fam["seed_note"],
    }


def build():
    families = [external_entry(f) for f in EXTERNAL]
    families += [generator_entry(f) for f in GENERATOR]
    return {
        "version": 1,
        "note": ("Counting-benchmark bank manifest. GENERATED by "
                 "benchmarks/bank_manifest_gen.py from benchmarks/bank_families.py "
                 "(the spec) merged with each family's bank/<id>/provenance.json. "
                 "Downloaded/converted data lives under benchmarks/bank/ and is "
                 "GITIGNORED — rerun the fetch_*.py scripts (e.g. on HPC2) to "
                 "materialize, then rerun this to pin archive hashes + counts. "
                 "Contract: docs/research/2026-07-13-counting-benchmark-survey.md."),
        "generated_utc": datetime.datetime.now(datetime.timezone.utc)
        .strftime("%Y-%m-%dT%H:%M:%SZ"),
        "disk_cap_gb": 12,
        "families": families,
    }


def main():
    ap = argparse.ArgumentParser(
        description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--dry-run", action="store_true")
    args = ap.parse_args()

    manifest = build()
    ext = [f for f in manifest["families"] if f["kind"] == "external"]
    gen = [f for f in manifest["families"] if f["kind"] == "generator"]
    mat = sum(1 for f in ext if f["materialized"])
    deferred = sum(1 for f in ext if f["deferred_until_m3"])
    print(f"{len(manifest['families'])} families: {len(ext)} external "
          f"({mat} materialized, {deferred} deferred-M3), {len(gen)} generator")
    for f in ext:
        flag = "M3" if f["deferred_until_m3"] else "  "
        state = "MAT" if f["materialized"] else "spec"
        print(f"  [{flag}] {state} {f['id']:<18} {f['license']}")
    for f in gen:
        print(f"       gen  {f['id']:<18} {f['mechanism']}")

    if args.dry_run:
        return 0
    with open(MANIFEST, "w") as fh:
        json.dump(manifest, fh, indent=2)
        fh.write("\n")
    print(f"wrote {os.path.relpath(MANIFEST, bc.ROOT)}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
