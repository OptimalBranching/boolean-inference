#!/usr/bin/env python3
"""Shared plumbing for the counting-benchmark bank fetch/convert pipeline.

The bank itself (downloaded archives + converted wcn-1 instances) lives under
`benchmarks/bank/` and is .gitignored — it is REGENERATED by rerunning the
`fetch_*.py` scripts, never committed. What IS committed: these scripts, the
converters, `bank_families.py` (the spec), and `bank_manifest.json` (generated).

Design guarantees this module enforces:
  * idempotent downloads — a file whose sha256 already matches is skipped; a
    partial download is resumed (`curl -C -`).
  * provenance — after materializing a family, `write_provenance()` records the
    real sha256 of every archive plus counts into
    `bank/<family_id>/provenance.json`, which `bank_manifest_gen.py` merges into
    the committed manifest.

Everything shells out to `curl` (handles Zenodo redirects/resume/TLS far better
than urllib) and the system `unzip`/`tar`/`xz`.
"""
import hashlib
import json
import os
import subprocess
import sys

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
BANK_DIR = os.path.join(ROOT, "benchmarks", "bank")


def bank_family_dir(family_id):
    d = os.path.join(BANK_DIR, family_id)
    os.makedirs(d, exist_ok=True)
    return d


def downloads_dir(family_id):
    d = os.path.join(bank_family_dir(family_id), "downloads")
    os.makedirs(d, exist_ok=True)
    return d


def sha256_file(path):
    h = hashlib.sha256()
    with open(path, "rb") as f:
        for chunk in iter(lambda: f.read(1 << 20), b""):
            h.update(chunk)
    return h.hexdigest()


def have_curl():
    return subprocess.run(["which", "curl"], capture_output=True).returncode == 0


def download(url, dest, expected_sha256=None, force=False):
    """Idempotently fetch `url` -> `dest`. Returns the sha256 of the final file.

    Skips the download when `dest` already exists and (a) no expected hash is
    given, or (b) the existing file's hash matches. Otherwise resumes/redownloads
    with `curl -L -C -`. Raises on network failure or a post-download hash
    mismatch (a corrupted/served-wrong archive must fail loudly, not silently
    poison the bank).
    """
    if os.path.exists(dest) and not force:
        got = sha256_file(dest)
        if expected_sha256 is None or got == expected_sha256:
            print(f"  [skip] {os.path.basename(dest)} already present "
                  f"(sha256 {got[:12]}…)")
            return got
        print(f"  [rehash mismatch] {os.path.basename(dest)}: re-downloading")
    if not have_curl():
        raise RuntimeError("curl not found on PATH; required for bank fetch")
    print(f"  [get ] {url}")
    os.makedirs(os.path.dirname(dest), exist_ok=True)
    cmd = ["curl", "-L", "--fail", "--retry", "5", "--retry-delay", "5",
           "-C", "-", "-o", dest, url]
    proc = subprocess.run(cmd)
    if proc.returncode != 0:
        raise RuntimeError(
            f"download failed ({proc.returncode}) for {url}\n"
            "  (this sandbox may have no network egress; run on a host with "
            "internet — e.g. HPC2 — to materialize the bank)")
    got = sha256_file(dest)
    if expected_sha256 is not None and got != expected_sha256:
        raise RuntimeError(
            f"sha256 mismatch for {dest}: expected {expected_sha256}, got {got}")
    print(f"  [ok  ] {os.path.basename(dest)} sha256 {got[:12]}…")
    return got


def zenodo_files(record_id):
    """Discover a Zenodo record's files via the public API (no hardcoded
    filenames — Zenodo download URLs embed the exact file key we cannot verify
    offline). Returns a list of dicts {key, url, md5, size}. Zenodo publishes an
    md5 per file; we still compute + pin our own sha256 after download."""
    if not have_curl():
        raise RuntimeError("curl not found on PATH")
    api = f"https://zenodo.org/api/records/{record_id}"
    proc = subprocess.run(["curl", "-L", "--fail", "-s", api],
                          capture_output=True, text=True)
    if proc.returncode != 0:
        raise RuntimeError(
            f"Zenodo API fetch failed for record {record_id} "
            f"(exit {proc.returncode}); no network egress?")
    data = json.loads(proc.stdout)
    out = []
    for f in data.get("files", []):
        out.append({
            "key": f.get("key"),
            "url": f.get("links", {}).get("self")
            or f"https://zenodo.org/records/{record_id}/files/"
            f"{f.get('key')}?download=1",
            "md5": (f.get("checksum") or "").replace("md5:", ""),
            "size": f.get("size"),
        })
    return out


def list_archive_members(archive):
    """List member paths inside a tar/zip archive without extracting (used to
    selectively pull only MCC tracks 1-2 out of a multi-track archive)."""
    name = archive.lower()
    if name.endswith(".zip"):
        proc = subprocess.run(["unzip", "-Z1", archive],
                              capture_output=True, text=True, check=True)
    else:
        proc = subprocess.run(["tar", "-tf", archive],
                              capture_output=True, text=True, check=True)
    return [ln for ln in proc.stdout.splitlines() if ln.strip()]


def unpack(archive, dest_dir, members=None):
    """Extract `archive` (.zip / .tar.* / .xz) into `dest_dir`. `members`, when
    given, is a list of path prefixes to selectively extract (used to pull only
    tracks 1-2 out of the 60 GB MCC-2024 archive). Idempotent enough: extraction
    is cheap and overwrites; callers gate on the archive hash, not this."""
    os.makedirs(dest_dir, exist_ok=True)
    name = archive.lower()
    if name.endswith(".zip"):
        cmd = ["unzip", "-o", "-q", archive, "-d", dest_dir]
        if members:
            cmd += members
        subprocess.run(cmd, check=True)
    elif name.endswith((".tar.gz", ".tgz", ".tar.xz", ".txz", ".tar.bz2",
                        ".tar")):
        cmd = ["tar", "-xf", archive, "-C", dest_dir]
        if members:
            cmd += members
        subprocess.run(cmd, check=True)
    elif name.endswith(".xz"):
        # bare .xz (single file): decompress next to it.
        subprocess.run(["xz", "-dk", "-f", archive], check=True)
    else:
        raise RuntimeError(f"don't know how to unpack {archive}")


def count_files(root, suffixes):
    n = 0
    for dp, _dn, fns in os.walk(root):
        for fn in fns:
            if fn.lower().endswith(tuple(suffixes)):
                n += 1
    return n


def write_provenance(family_id, archives, extra=None):
    """Record what a fetch actually produced. `archives` maps filename->sha256.
    `extra` is merged in (counts, convertible/skipped tallies, notes)."""
    prov = {
        "family_id": family_id,
        "archives": archives,
    }
    if extra:
        prov.update(extra)
    path = os.path.join(bank_family_dir(family_id), "provenance.json")
    with open(path, "w") as f:
        json.dump(prov, f, indent=2)
        f.write("\n")
    print(f"  [prov] wrote {os.path.relpath(path, ROOT)}")
    return path


def read_provenance(family_id):
    path = os.path.join(BANK_DIR, family_id, "provenance.json")
    if not os.path.exists(path):
        return None
    with open(path) as f:
        return json.load(f)


def die(msg):
    print(f"error: {msg}", file=sys.stderr)
    sys.exit(1)
